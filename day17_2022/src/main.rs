fn decode_input(input: &str) -> Vec<Move> {
    input.split_terminator('\n').filter(|l| !l.is_empty()).fold(
        Vec::with_capacity(input.len()),
        |mut acc, l| {
            l.as_bytes().iter().for_each(|b| {
                acc.push(match b {
                    b'<' => Move::Left,
                    b'>' => Move::Right,
                    _ => panic!("unexpected input!"),
                })
            });
            acc
        },
    )
}

